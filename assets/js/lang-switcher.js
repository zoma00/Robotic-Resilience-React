  function loadLang(lang) {
    console.log('ğŸŒ Loading language:', lang);
    const dict = TRANSLATIONS[lang] || TRANSLATIONS['en'];
    console.log('ğŸ“š Dictionary loaded with', Object.keys(dict).length, 'keys');
    applyLang(dict, lang);
    
    // Force a second translation pass after a delay to catch any dynamically loaded content
    setTimeout(() => {
      console.log('ğŸ”„ Second translation pass...');
      applyLang(dict, lang);
    }, 500);
  }

  function applyLang(dict, lang) {
    console.log('ğŸ”„ Applying language:', lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    
    // Store the current language for TTS system
    localStorage.setItem('selectedLanguage', lang);
    
    let translatedCount = 0;
    let skippedCount = 0;
    const skippedKeys = [];
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (dict[key]) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = dict[key];
        } else {
          el.textContent = dict[key];
        }
        translatedCount++;
      } else {
        skippedCount++;
        skippedKeys.push(key);
        console.warn('âŒ Missing translation for key:', key);
      }
    });
    
    console.log(`âœ… Translated ${translatedCount} elements to language: ${lang}`);
    if (skippedCount > 0) {
      console.warn(`âš ï¸ Skipped ${skippedCount} elements with missing translations`);
      console.log('Missing keys:', skippedKeys.slice(0, 10)); // Show first 10
    }
    
    // Refresh TTS system for new language (if available)
    if (typeof window.refreshTTSForLanguage === 'function') {
      setTimeout(() => {
        window.refreshTTSForLanguage();
      }, 100); // Small delay to ensure DOM updates are complete
    }
  }

